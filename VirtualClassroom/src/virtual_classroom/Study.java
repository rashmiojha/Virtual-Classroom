    public void chararray(char r[])
    {
        try {
            for(int i=0;i<r.length;i++)
            {
                toSocket.writeChar(r[i]);
            }
            toSocket.writeChar('$');
        }
        catch (IOException ex) {
                Logger.getLogger(Study.class.getName()).log(Level.SEVERE, null, ex);
         }
    }  
