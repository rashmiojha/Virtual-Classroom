private void readFile(String tab, String fname, String path)
{
   try{
                  client = new Socket("localhost",2015);
                  fromSocket = new DataInputStream(client.getInputStream());
                  toSocket = new DataOutputStream(client.getOutputStream());

                  chararray("FileDownload".toCharArray());
                  chararray(tab.toCharArray());
                  chararray(fname.toCharArray());
                  String extension = new String(read());
                  byte [] b = new byte[fromSocket.readInt()];
                  for(int i = 0;i<b.length;i++)
                    b[i] = fromSocket.readByte();
                  toFile = new DataOutputStream(new FileOutputStream(path+extension));
                  toFile.write(b);
                  toFile.close();
                  String status = new String(read());
                  if(status.equals("DownloadComplete"))
                      JOptionPane.showMessageDialog(this, "Your Download is complete", "Download Successful", JOptionPane.OK_OPTION);
                  else if(status.equals("DownloadUnsuccessful"))
                      JOptionPane.showMessageDialog(this, "Your Download was unsuccessful. Please try  again later", "Download Unsuccessful", JOptionPane.ERROR_MESSAGE);
       }
   catch(IOException | HeadlessException e)
   {
       e.printStackTrace();
   }
 }
